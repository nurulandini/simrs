    /**
     * Lists all DataJadwalPegawai models.
     * @return mixed
     */
    public function actionIndex()
    {
        $searchModel = new DataJadwalPegawaiSearch();
        $dataProvider = $searchModel->search(Yii::$app->request->queryParams);


        // Ambil semua jadwal pegawai 
        $jadwal = DataJadwalPegawai::find()->orderBy(['pegawai_id' => SORT_DESC])->all();

        if (!is_array($jadwal)) {
            $jadwal = []; // Pastikan selalu array
        }

        $groupedJadwal = [];
        foreach ($jadwal as $item) {
            $pegawaiId = $item->pegawai_id;

            if (!isset($groupedJadwal[$pegawaiId])) {
                $groupedJadwal[$pegawaiId] = [
                    'pegawai' => $item->pegawai, // Data Pegawai
                    'jadwal' => [],
                ];
            }

            $groupedJadwal[$pegawaiId]['jadwal'][] = [
                'id' => $item->id,
                'hari' => $item->hari_kerja,
                'shift' => $item->shift,
                'jam_mulai' => $item->mulai,
                'jam_akhir' => $item->akhir,
            ];
        }
        return $this->render('index', [
            'searchModel' => $searchModel,
            'dataProvider' => $dataProvider,
            'groupedJadwal' => $groupedJadwal,
        ]);
    }


    /**
     * Displays a single DataJadwalPegawai model.
     * @param integer $id
     * @return mixed
     */
    public function actionView($id)
    {
        $request = Yii::$app->request;
        if ($request->isAjax) {
            Yii::$app->response->format = Response::FORMAT_JSON;
            return [
                'title' => "DataJadwalPegawai #" . $id,
                'content' => $this->renderAjax('view', [
                    'model' => $this->findModel($id),
                ]),
                'footer' => Html::button(Yii::t('yii2-ajaxcrud', 'Close'), ['class' => 'btn btn-default pull-left', 'data-dismiss' => 'modal']) .
                    Html::a(Yii::t('yii2-ajaxcrud', 'Update'), ['update', 'id' => $id], ['class' => 'btn btn-primary', 'role' => 'modal-remote'])
            ];
        } else {
            return $this->render('view', [
                'model' => $this->findModel($id),
            ]);
        }
    }

    /**
     * Creates a new DataJadwalPegawai model.
     * For ajax request will return json object
